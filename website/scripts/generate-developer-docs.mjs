// Cross-links between docs pages use plain relative `./file.md` links —
// correct as-is on GitHub, where these files are read directly. Rewrite them
// to absolute site paths so they also resolve on the published site.
const SITE_PATH_BY_SRC = Object.fromEntries(
  Object.entries(MANIFEST).flatMap(([section, { pages }]) =>
    pages.map(p => [p.src, `/developer/${section}/${p.slug}`])
  )
)

function rewriteLinks(markdown) {
  return markdown.replace(/\]\((?:\.\/)?([\w-]+\.md)\)/g, (match, src) => {
    const sitePath = SITE_PATH_BY_SRC[src]
    return sitePath ? `](${sitePath})` : match
  })
}

const blobUrl = src =>
  `https://github.com/smbcloudXYZ/smbcloud-cli/blob/${BRANCH}/${SRC_DIR}/${src}`

/**
 * Wrap generated markdown: frontmatter (for real per-page SEO metadata),
 * header note, and a source callout after the first H1.
 */
function decorate(markdown, page) {
  markdown = rewriteLinks(markdown)
  const frontmatter = [
    '---',
    `title: ${JSON.stringify(page.title)}`,
    `description: ${JSON.stringify(page.description)}`,
    '---'
  ].join('\n')
  const note = `{/* AUTO-GENERATED — do not edit. Generated by scripts/generate-developer-docs.mjs from ../${SRC_DIR}/${page.src}. */}`
  const callout = [
    "import { Callout } from 'nextra/components'",
    '',
    '<Callout type="info">',
    `  Generated from [\`${SRC_DIR}/${page.src}\`](${blobUrl(page.src)}) in`,
    '  [smbcloud-cli](https://github.com/smbcloudXYZ/smbcloud-cli). Edit it there.',
    '</Callout>'
  ].join('\n')

  const lines = markdown.replace(/\r\n/g, '\n').trimEnd().split('\n')
  const h1 = lines.findIndex(l => /^#\s/.test(l))
  if (h1 === -1) {
    return `${frontmatter}\n\n${note}\n\n${callout}\n\n${lines.join('\n')}\n`
  }
  const before = lines.slice(0, h1 + 1).join('\n')
  const after = lines.slice(h1 + 1).join('\n')
  return `${frontmatter}\n\n${note}\n\n${before}\n\n${callout}\n${after}\n`
}

// Note: the folder's own index page uses `asIndexPage` frontmatter, so it must
// NOT be listed as an `index` key here — Nextra surfaces it via the parent.
function metaFile(pages) {
  const entries = pages.map(p => `  ${p.slug}: ${JSON.stringify(p.title)},`)
  return `export default {\n${entries.join('\n')}\n}\n`
}

function main() {
  let count = 0
  for (const [section, { title, pages }] of Object.entries(MANIFEST)) {
    const dir = join(DEST, section)
    mkdirSync(dir, { recursive: true })
    for (const page of pages) {
      const raw = readFileSync(join(SRC, page.src), 'utf8')
      writeFileSync(join(dir, `${page.slug}.mdx`), decorate(raw, page))
      count++
    }
    writeFileSync(join(dir, '_meta.js'), metaFile(pages))
    console.log(`  ${section}: ${pages.length} pages (${title})`)
  }

  // Top-level developer nav: each section (the overview is asIndexPage).
  const top = []
  for (const [section, { title }] of Object.entries(MANIFEST)) {
    top.push(`  ${section}: ${JSON.stringify(title)},`)
  }
  writeFileSync(join(DEST, '_meta.js'), `export default {\n${top.join('\n')}\n}\n`)

  console.log(`\nGenerated ${count} pages into content/developer/ from ${SRC}.`)
}

main()
