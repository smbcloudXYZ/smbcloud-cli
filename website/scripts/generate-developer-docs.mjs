#!/usr/bin/env node
/**
 * Generate the /developer docs from the sibling `docs/` tree.
 *
 * The CLI / tooling documentation lives in `docs/` at the smbcloud-cli repo root
 * (this site is `website/` in the same repo), so it is read in place — no clone,
 * no network. This runs automatically on `predev`/`prebuild`; the generated
 * pages are gitignored, leaving `docs/` as the single source of truth.
 *
 *   pnpm docs:generate
 *
 * Hand-written index pages (content/developer/**\/index.mdx) are NOT touched.
 * Only the files listed in MANIFEST and the generated _meta.js are written.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const BRANCH = 'main'
const SRC_DIR = 'docs'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
// `docs/` sits next to `website/` at the smbcloud-cli repo root.
